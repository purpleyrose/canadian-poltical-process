	private void SetPolicyOptions(string Preset)
	{
		if (!PolicyPresets.ContainsKey(Preset))
		{
			GD.PrintErr("Policy preset not found: " + Preset);
			return;
		}
		foreach (Node node in GetTree().GetNodesInGroup("PolicyGroup"))
		{
			if (node is CheckBox checkBox)
			{
				string PolicyName = checkBox.GetParent().GetParent().Name;
				if (PolicyPresets[Preset].ContainsKey(PolicyName)) // Check if our preset contains the policy
				{
					object PresetValue = PolicyPresets[Preset][PolicyName];
					if (PresetValue is bool isYes) // Corresponds to true
					{
						checkBox.SetPressedNoSignal((checkBox.Text == "Yes") == isYes);
					}
					else if (PresetValue is string PresetString)
					{
						checkBox.SetPressedNoSignal(checkBox.Text == PresetString);
					}
					GD.Print($"Set policy {PolicyName} to {PresetValue}");
				}
			}
		}
	}

    private void ClosePanel()
    {
        Visible = false;
        Policies.Visible = true;
    }
