    {
        Policies = GetNodeOrNull<VBoxContainer>("../ScrollContainer/PolicyBox");
        if (Policies == null)
        {
            GD.PrintErr("Policies container not found.");
        }
        else
        {
            GD.Print("Policies container successfully assigned.");
        }
        Visible = false;
        Policies.Visible = true;
    }

